package testing;

public class ExampleClass {

	/**
	 * multiplies two numbers
	 * @param x
	 * @param y
	 * @return
	 */
	public int multiplyNumbers(int x, int y){
		return x*y;
	}

	/**
	 * concatenates two strings
	 * @param a
	 * @param b
	 * @return
	 */
	public String concatenateStrings(String a, String b){
		return a+b;
	}
	
}
